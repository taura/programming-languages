long add(long x, long y, long z) {
  return x + 2 * y + 3 * z;
}

long sub(long x, long y, long z) {
  return x - y - z;
}

long mul(long x, long y, long z) {
  return x * y * z;
}

long div(long x, long y, long z) {
  return x / y / z;
}

